b = "Hello, World!"

print(b[1])

#from 2 (included) to position 5 (not included)
print(b[2:5])

#from the start to position 5 (not included)
print(b[:5])

print(b[2:])

#use Negative indexes to start from the end
print(b[-5:-2])
#from "o" in "World!" (positition -5)
#to, but not included: "d" in "World!" (position -2)

